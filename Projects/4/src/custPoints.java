//Custom class to hold information on each paint point
import java.awt.Color;

public class custPoints {
	int _x, _y, _size;
	Color _color;
	public custPoints(Color color, int x, int y, int size){
		_color = color;
		_x = x;
		_y = y;
		_size = size;
	}	
}